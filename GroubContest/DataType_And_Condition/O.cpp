#include<bits/stdc++.h>
using namespace std;

int main()
{
    int f,l;
    char s;
    cin>>f>>s>>l;
    if(s=='+')
    {
        cout<<(f+l)<<endl;
    }
    else if(s=='-')
    {
        cout<<(f-l)<<endl;
    }
    else if(s=='*')
    {
         cout<<(f*l)<<endl;
    }
    else
    {
        cout<<(f/l)<<endl;
    }










}
